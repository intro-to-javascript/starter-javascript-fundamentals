/*
  DOUBLE EQUALS VERSUS TRIPLE EQUALS
  == vs ===
  != vs !==
  Double equals doesn’t check for type, so it won’t care if the data types 
  are the same.
  
  Triple equals — the strict equality operator — will compare both the 
  value and data type, creating much more predictable results. Just use 
  triple equals!
*/
console.log(2 == "2");
console.log(2 === "2");

